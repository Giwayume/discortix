<script setup lang="ts">
import { onMounted, onBeforeUnmount, onUnmounted, ref } from 'vue'

const emit = defineEmits<{
    (e: 'intersecting', value: boolean): void
}>()

const container = ref<HTMLDivElement>()
let intersectionObserver: IntersectionObserver | undefined

onMounted(() => {
    if (!container.value) return

    intersectionObserver = new IntersectionObserver(onIntersect, {
        root: container.value.closest('.p-scrollpanel-content'),
        rootMargin: '500px 0px 500px 0px',
        threshold: 0.0,
    })
    intersectionObserver.observe(container.value)
})

onBeforeUnmount(() => {
    emit('intersecting', false)
})

onUnmounted(() => {
    intersectionObserver?.disconnect()
})

function onIntersect(entries: IntersectionObserverEntry[], observer: IntersectionObserver) {
    if (entries[0]?.isIntersecting) {
        emit('intersecting', true)
    } else {
        emit('intersecting', false)
    }
}

</script>